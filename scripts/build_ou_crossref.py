db = sqlite3.connect("D:/AI/footballAI/data/football_data.db")
cur = db.cursor()

OU_DB = {}
for wcm in WC_MATCHES:
    oid, spread = wcm['oid'], wcm['spread']
    cur.execute("""
        SELECT m.home_score, m.away_score FROM matches m
        JOIN match_features mf ON m.match_id = mf.match_id
        WHERE m.home_score IS NOT NULL AND mf.odds_imp_d IS NOT NULL
          AND ABS(mf.odds_imp_d - ?) < 0.03
          AND ABS(mf.odds_spread - ?) < 0.05
        LIMIT 200
    """, (oid, spread))
    similar = cur.fetchall()
    
    if similar:
        d_bin = round(oid / 0.02) * 0.02
        s_bin = round(spread / 0.05) * 0.05
        key = f"d{d_bin:.2f}_s{s_bin:.2f}"
        if key not in OU_DB:
            OU_DB[key] = {"wc":[], "hist":[]}
        OU_DB[key]["wc"].append(wcm['total'])
        for hsc, asc in similar:
            OU_DB[key]["hist"].append((hsc or 0) + (asc or 0))

print(f"OU数据库: {len(OU_DB)}个特征组合\n")

# 分析
print(f"{'d_prob':8s} {'spread':8s} {'WC':>4s} {'Wavg':>6s} {'hist':>6s} {'Havg':>6s} {'status':>6s}")
total_wc = 0; total_hist = 0; correct = 0; count = 0
results = []
for key in sorted(OU_DB.keys()):
    val = OU_DB[key]
    wc_avg = sum(val["wc"])/len(val["wc"]) if val["wc"] else 0
    hist_avg = sum(val["hist"])/len(val["hist"]) if val["hist"] else 0
    d_p = float(key[1:].split('_')[0])
    s_p = float(key.split('_')[1][1:])
    diff = abs(wc_avg - hist_avg)
    status = 'OK' if diff<0.5 else ('WARN' if diff<1.0 else 'GAP')
    if len(val["wc"]) >= 1:
        print(f"  d={d_p:.2f} s={s_p:.2f} {len(val['wc']):>3}场 {wc_avg:>5.1f}球 {len(val['hist']):>5}场 {hist_avg:>5.1f}球 {status:>6s}")
    total_wc += len(val["wc"]); total_hist += len(val["hist"])
    
    # 预测验证
    for actual in val["wc"]:
        count += 1
        pred = round(hist_avg)
        if abs(pred - actual) <= 1:
            correct += 1
    results.append((d_p, s_p, len(val["wc"]), wc_avg, len(val["hist"]), hist_avg, Counter(val["hist"])))

db.close()

print(f"\nWC覆盖: {total_wc}场 | 历史样本: {total_hist}场 | bins: {len(OU_DB)}")
print(f"预测准确(+-1球): {correct}/{count} = {correct/count*100:.0f}%")

