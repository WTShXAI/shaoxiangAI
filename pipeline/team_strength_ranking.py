def compute_elo(iteration_count=20):
    """基于34场赛果+让球加权计算Elo"""
    elo = defaultdict(lambda: 1500)
    
    for _ in range(iteration_count):
        for home, away, hg, ag, hcp in COMPLETED:
            diff = elo[home] - elo[away]
            exp_h = 1 / (1 + 10**(-diff/400))
            
            if hg > ag: actual = 1.0
            elif ag > hg: actual = 0.0
            else: actual = 0.5
            
            # K值: 比分差越大K越小 (屠杀=实力差已反映)
            margin = abs(hg - ag)
            k = 32 / (1 + margin * 0.15)  # 大胜不加太多分
            k = max(k, 16)
            
            # 让球加权: 打破让球=超额加分
            if hg > ag:
                cover = (hg - ag) > abs(hcp)
                if cover: k *= 1.3  # 打破让球=超额实力
            
            elo[home] += k * (actual - exp_h)
            elo[away] -= k * (actual - exp_h)
    
    return elo

