"""
哨响AI v5.0 — 外部数据增强规则框架
=====================================
设计原则: 不修改模型, 不影响核心推理, 只做后处理信号增强。
所有规则通过 predict_hybrid() 的 override 机制生效。

数据来源 (待实现):
  - 球队近期战绩 (qtx/球探网/Flashscore)
  - FIFA排名 (已有: config/fifa_rankings_2026.json)
  - 伤病/阵容 (新闻API)
  - 交锋历史 (h2h)
"""

# ═══════════════════════════════════════════════════════════
# 规则1: 球队形态信号 (Team Form Signal)
# ═══════════════════════════════════════════════════════════
"""
触发条件: 可获取到两队近5场完整战绩
输入: home_form={W/D/L}, away_form={W/D/L}
输出: form_signal ∈ {'STRONG_H', 'STRONG_A', 'BALANCED', 'UNKNOWN'}

规则:
  - 主队近5场4W+ 且 客队近5场2L+ → STRONG_H
    → 当JEPA预测D且KNN预测A时, 覆盖为H (主场龙不可忽视)
  - 客队近5场4W+ 且 主队近5场2L+ → STRONG_A
    → 当JEPA预测D且KNN预测H时, 覆盖为A
  - 其他 → BALANCED (不干预)
"""

# ═══════════════════════════════════════════════════════════
# 规则2: FIFA排名差信号 (Rank Gap Signal)
# ═══════════════════════════════════════════════════════════
"""
触发条件: 两队FIFA排名均可获取
输入: rank_home, rank_away
输出: rank_signal ∈ {'HUGE_GAP', 'CLOSE', 'UNKNOWN'}

规则 (已验证的数据, 仅用于置信度调整):
  - |rank_diff| > 50 → HUGE_GAP
    → 降低平局预测置信度, draw_signal降一级
    → 不覆盖预测, 但前端显示"排名悬殊, 平局风险高"
  - |rank_diff| < 10 → CLOSE  
    → 提高平局预测置信度, draw_signal升一级(最多到STRONG)
  - 其他 → 不干预
"""

# ═══════════════════════════════════════════════════════════
# 规则3: 交锋历史信号 (H2H Signal)
# ═══════════════════════════════════════════════════════════
"""
触发条件: 可获取到近3次交锋结果
输入: h2h_results=['H','A','D'] (按时间倒序)
输出: h2h_signal ∈ {'DRAW_PATTERN', 'ONE_SIDED', 'UNKNOWN'}

规则:
  - 近3次交锋中≥2次平局 → DRAW_PATTERN
    → 强制将draw_signal提升为STRONG
  - 近3次交锋同队全胜 → ONE_SIDED
    → 降低平局置信度 (不覆盖预测, 仅降draw_signal)
  - 其他 → 不干预
"""

# ═══════════════════════════════════════════════════════════
# 规则4: 赛前新闻/伤病 (News Signal)
# ═══════════════════════════════════════════════════════════
"""
触发条件: 检测到关键球员缺席新闻
输入: key_absence ∈ {'HOME', 'AWAY', 'BOTH', 'NONE'}
输出: news_signal ∈ {'WEAKEN_H', 'WEAKEN_A', 'NORMAL'}

规则:
  - HOME关键球员缺席 → WEAKEN_H
    → KNN预测H时覆盖为D (主队实力受损)
  - AWAY关键球员缺席 → WEAKEN_A
    → KNN预测A时覆盖为D (客队实力受损)
  - BOTH/NONE → 不干预
"""

# ═══════════════════════════════════════════════════════════
# 集成优先级 (从高到低)
# ═══════════════════════════════════════════════════════════
"""
1. 伤病/阵容信号 (规则4) — 关键球员缺席直接改变战力
2. 形态信号 (规则1) — 近期状态是赛前最强预测因子
3. JEPA平局覆盖 (现有) — 模型对平局有独立判断力
4. KNN历史基准 (现有) — 数据驱动的默认预测
5. FIFA排名 (规则2) — 仅调整置信度, 不改变预测
6. 交锋历史 (规则3) — 仅调整置信度, 不改变预测
"""
